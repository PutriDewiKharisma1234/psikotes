            <h2>MBTI (Myers-Briggs Type Indicator)</h2>
            <p>MBTI adalah sebuah tes psikologi yang didasarkan pada teori tipe kepribadian yang dikembangkan oleh Carl Jung dan dipopulerkan oleh Isabel Briggs Myers dan ibunya, Katharine Cook Briggs. Tes ini membagi kepribadian manusia menjadi 16 tipe yang berbeda berdasarkan empat dimensi utama:</p>

            <h3>1. Ekstravert (E) vs Introvert (I)</h3>
            <p>Dimensi ini menggambarkan bagaimana seseorang mendapatkan energi. Ekstravert cenderung mendapatkan energi dari interaksi dengan orang lain dan kegiatan di luar, sementara introvert cenderung lebih suka waktu sendiri atau dengan sedikit orang. Mereka mendapatkan energi dengan cara yang berbeda:</p>
            <ul>
                <li><strong>Ekstravert (E):</strong> Lebih fokus pada dunia luar, aktif, berbicara dengan orang lain untuk berpikir, dan mendapatkan energi dari interaksi sosial.</li>
                <li><strong>Introvert (I):</strong> Lebih fokus pada dunia internal, berpikir terlebih dahulu sebelum berbicara, dan mendapatkan energi dari waktu sendiri.</li>
            </ul>

            <h3>2. Sensing (S) vs Intuition (N)</h3>
            <p>Dimensi ini menggambarkan bagaimana seseorang memproses informasi. Sensing berfokus pada informasi yang nyata dan konkret, sedangkan Intuition berfokus pada kemungkinan dan konsep:</p>
            <ul>
                <li><strong>Sensing (S):</strong> Cenderung memperhatikan detail dan fakta yang ada di sekitar mereka.</li>
                <li><strong>Intuition (N):</strong> Cenderung lebih tertarik pada gambaran besar, pola, dan kemungkinan masa depan.</li>
            </ul>

            <h3>3. Thinking (T) vs Feeling (F)</h3>
            <p>Dimensi ini menggambarkan bagaimana seseorang membuat keputusan. Thinking lebih berfokus pada logika dan analisis, sedangkan Feeling lebih memperhatikan nilai dan emosi:</p>
            <ul>
                <li><strong>Thinking (T):</strong> Memutuskan dengan logika, fakta, dan rasionalitas.</li>
                <li><strong>Feeling (F):</strong> Memutuskan dengan mempertimbangkan perasaan dan dampaknya terhadap orang lain.</li>
            </ul>

            <h3>4. Judging (J) vs Perceiving (P)</h3>
            <p>Dimensi ini menggambarkan bagaimana seseorang berinteraksi dengan dunia luar. Judging lebih menyukai struktur dan keputusan yang sudah dibuat, sedangkan Perceiving lebih suka fleksibilitas dan keterbukaan terhadap kemungkinan baru:</p>
            <ul>
                <li><strong>Judging (J):</strong> Cenderung lebih terorganisir, menyukai perencanaan, dan membuat keputusan dengan cepat.</li>
                <li><strong>Perceiving (P):</strong> Cenderung fleksibel, terbuka terhadap perubahan, dan lebih suka menunda keputusan untuk mempertimbangkan lebih banyak informasi.</li>
            </ul>

            <p>Dengan mengkombinasikan keempat dimensi ini, MBTI menghasilkan 16 tipe kepribadian yang masing-masing memiliki karakteristik unik. Tes ini sangat berguna untuk memahami cara seseorang berinteraksi dengan dunia, bekerja, dan mengambil keputusan.</p>
