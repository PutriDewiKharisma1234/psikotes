            <h2>Big Five Personality Traits (OCEAN)</h2>
            <p>Big Five Personality Traits, atau yang juga dikenal dengan nama OCEAN, adalah teori psikologi yang menggambarkan lima dimensi utama kepribadian manusia. Model ini digunakan secara luas untuk menilai kepribadian seseorang dalam konteks yang lebih luas dan objektif. Berikut adalah lima dimensi utama dalam model OCEAN:</p>

            <h3>1. Openness (Keterbukaan)</h3>
            <p>Dimensi ini menggambarkan sejauh mana seseorang terbuka terhadap pengalaman baru dan ide-ide kreatif. Orang yang memiliki skor tinggi dalam keterbukaan cenderung suka bereksperimen, berpikir kreatif, dan tertarik pada hal-hal baru, sedangkan mereka yang memiliki skor rendah lebih menyukai rutinitas dan cara-cara yang sudah terbukti.</p>

            <h3>2. Conscientiousness (Ketekunan)</h3>
            <p>Dimensi ini menunjukkan sejauh mana seseorang terorganisir, bertanggung jawab, dan dapat diandalkan. Orang dengan skor tinggi dalam ketekunan biasanya sangat terstruktur, berhati-hati dalam membuat keputusan, dan berorientasi pada pencapaian tujuan. Sebaliknya, mereka yang memiliki skor rendah lebih cenderung tidak terorganisir dan fleksibel dalam pendekatan mereka terhadap tugas.</p>

            <h3>3. Extraversion (Ekstraversi)</h3>
            <p>Ekstraversi menggambarkan sejauh mana seseorang terlibat dalam interaksi sosial dan kegiatan luar. Individu dengan skor tinggi dalam ekstraversi cenderung suka bersosialisasi, energik, dan terbuka, sedangkan mereka yang memiliki skor rendah lebih introvert, cenderung menghindari keramaian, dan lebih memilih untuk bekerja secara independen.</p>

            <h3>4. Agreeableness (Keterbukaan terhadap Orang Lain)</h3>
            <p>Dimensi ini menunjukkan sejauh mana seseorang bersedia bekerja sama, mempercayai orang lain, dan menunjukkan empati. Orang dengan skor tinggi dalam agreeableness cenderung lebih ramah, kooperatif, dan mudah beradaptasi dengan orang lain, sedangkan mereka yang memiliki skor rendah mungkin lebih kompetitif dan skeptis terhadap orang lain.</p>

            <h3>5. Neuroticism (Neurotisisme)</h3>
            <p>Neurotisisme menggambarkan sejauh mana seseorang cenderung merasa cemas, khawatir, atau stres. Individu dengan skor tinggi dalam neurotisisme cenderung lebih emosional dan rentan terhadap stres, sedangkan mereka yang memiliki skor rendah lebih stabil secara emosional dan kurang cemas.</p>

            <p>Model Big Five ini sangat membantu dalam memprediksi perilaku seseorang dan memberikan wawasan lebih dalam tentang karakter individu dalam kehidupan sehari-hari.</p>
